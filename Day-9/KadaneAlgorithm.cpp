    long long maxSubarraySum(int arr[], int n){
        long long sum0=INT_MIN ,sum1=0;
        for(int i=0;i<n;i++){
            sum1+=arr[i];
            if(sum0<sum1){
                sum0=sum1;
            }
            if(sum1<0){
                sum1=0;
            }
        }
        // Your code here
    return sum0;
    }
